/* area of a parallelogram */

function areaOfParallelogram(breadth, height) {
	return breadth * height;

}
console.log(areaOfParallelogram(6, 3))


/* the volume of a cylinder */

function volumeOfCylinder(radius, height) {
	const pi = 3.142;
	return pi * (radius * radius) * height;
}

console.log(volumeOfCylinder(5, 9))



/* area of a rectangle */

function areaOfRectangle(width, height) {
	return width * height;
}

console.log(areaOfRectangle(10, 8))